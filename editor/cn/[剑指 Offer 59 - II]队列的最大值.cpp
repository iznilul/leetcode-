class MaxQueue {
public:
    deque<int> dq;
    queue<int> q;
    MaxQueue(){

    }
    int max_value() {
        if(dq.empty()) return -1;
        return dq.front();
    }

    void push_back(int value) {
        q.push(value);
        while(!dq.empty() && value > dq.back())
            dq.pop_back(); //ç»´æŠ¤åŒç«¯é˜Ÿåˆ—ç”±å¤§åˆ°å°çš„å•è°ƒæ€§
        dq.push_back(value);
    }

    int pop_front() {
        if(q.empty()) return -1;
        if(q.front() == dq.front())
            dq.pop_front();
        int res = q.front();
        q.pop();
        return res;
    }
};

