/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
class Solution {
public:
    vector<int> reversePrint(ListNode* head) {
        if(head== nullptr)
            return {};
        stack<int> stack;
        vector<int> v;
        while(head!= nullptr){
            stack.push(head->val);
            head = head->next;
        }
        while(!stack.empty()){
            v.push_back(stack.top());
            stack.pop();
        }
        return v;
    }
};
